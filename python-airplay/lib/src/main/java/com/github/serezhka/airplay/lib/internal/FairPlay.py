# Fixed 12-byte header returned in the 164-byte request branch
_FP_HEADER = bytes([70, 80, 76, 89, 3, 1, 4, 0, 0, 0, 0, 20])


class FairPlay:
    """
    对应 Java FairPlay 类。
    处理 AirPlay /fp-setup 握手的两个阶段，并在握手完成后提供 AES 密钥解密。
    """

    def __init__(self):
        self._omg_hax: OmgHax = OmgHax()
        # 保存 164 字节的 keyMsg（第二阶段请求内容），用于最终 AES 密钥解密
        self._key_msg: bytearray = bytearray(164)

    # ------------------------------------------------------------------
    # public void fairPlaySetup(InputStream request, OutputStream response)
    # ------------------------------------------------------------------
    def fair_play_setup(self, data: bytes) -> bytes:
        """
        处理 /fp-setup 请求。
        :param data:  请求体的原始字节（对应 Java 的 request.readAllBytes()）
        :return:      应写入响应的字节（对应 Java 的 response.write(...)）
        """
        # 版本检查：data[4] 必须是 3
        if data[4] != 3:
            log.error("FairPlay version %d is not supported!", data[4])
            return b""

        if len(data) == 16:
            # 第一阶段：根据 mode 回复预设的 142 字节消息
            mode = data[14]
            if mode < 0 or mode > 3:
                log.error("FairPlay unknown mode: %d", mode)
                return b""
            return _REPLY_MESSAGES[mode]

        elif len(data) == 164:
            # 第二阶段：保存全部 164 字节，返回 12 字节 header + data[144:164]
            self._key_msg[:] = data[:164]
            return _FP_HEADER + data[144:164]

        else:
            log.error("FairPlay unexpected data length: %d", len(data))
            return b""

    # ------------------------------------------------------------------
    # public byte[] decryptAesKey(byte[] key)
    # ------------------------------------------------------------------
    def decrypt_aes_key(self, key: bytes) -> bytes:
        """
        使用 OmgHax 解密 AES 密钥。
        :param key:  来自 RTSP SETUP 的加密 ekey（byte[]）
        :return:     16 字节明文 AES 密钥
        """
        aes_key = bytearray(16)
        self._omg_hax.decrypt_aes_key(bytes(self._key_msg), key, aes_key)
        log.info("FairPlay AES key decrypted: %s", aes_key.hex())
        return bytes(aes_key)
